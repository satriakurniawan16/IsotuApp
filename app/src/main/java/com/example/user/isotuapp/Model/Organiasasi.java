package com.example.user.isotuapp.Model;

public class Organiasasi {
    String id,nama;

    public Organiasasi() {
    }

    public Organiasasi(String id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
}
